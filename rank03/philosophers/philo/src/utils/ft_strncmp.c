int	ft_strncmp(const char *s1, const char *s2, unsigned int n)
{
	unsigned char	c1;
	unsigned char	c2;
	unsigned int	i;

	i = 0;
	if (n == 0)
		return (0);
	while (s1[i] == s2[i] && i < n - 1 && s1[i])
		i++;
	c1 = s1[i];
	c2 = s2[i];
	return (c1 - c2);
}
