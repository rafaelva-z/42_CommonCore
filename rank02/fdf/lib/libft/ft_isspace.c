/* 
ASCII REFERENCE

9  = \t
10 = \n
11 = \v
12 = \f
13 = \r
32 = SPACE
*/

int	ft_isspace(char c)
{
	if ((c >= 9 && c <= 13) || c == 32)
		return (1);
	return (0);
}
