#include "ft_printf.h"

size_t	ft_nputstr(char *s)
{
	size_t	i;

	i = 0;
	if (!s)
		return (ft_nputstr("(null)"));
	while (s[i])
		i += ft_nputchar(s[i]);
	return (i);
}
